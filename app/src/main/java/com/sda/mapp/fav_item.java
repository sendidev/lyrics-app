package com.sda.mapp;

public class fav_item {

    private String id2;
    private String title2;
    private String favs2;

    public fav_item(String id2, String title2, String favs2) {
        this.id2 = id2;
        this.title2 = title2;
        this.favs2 = favs2;
    }

    public String getId2() {
        return id2;
    }

    public void setId2(String id2) {
        this.id2 = id2;
    }

    public String getTitle2() {
        return title2;
    }

    public void setTitle2(String title2) {
        this.title2 = title2;
    }

    public String getFavs2() {
        return favs2;
    }

    public void setFavs2(String favs2) {
        this.favs2 = favs2;
    }
}
